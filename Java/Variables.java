
// Variables is used to store values temporarily.
public class Variables {
    public static void main(String[] args) {
        String Name = "Ronin";
        int age = 30;
        String Friend = "Jack";
        String Neighbour = Friend; // We can assign value of one variable to another variable as shown here.

        System.out.println("Name: " +Name);
        System.out.println("Age: " +age);
        System.out.println("Friend is: " +Friend);
        System.out.println("Neighbour is: " +Neighbour);

    }
}
